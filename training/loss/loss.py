class stack_HeatmapLoss(torch.nn.Module):
    
    def __init__(self, nstack):
        super(stack_HeatmapLoss, self).__init__()
        self.nstack      = nstack
        self.heatmapLoss = HeatmapLoss()

    def forward(self, combined_hm_preds, heatmaps):
        combined_loss = []
        for i in range(self.nstack):
            combined_loss.append(self.heatmapLoss(combined_hm_preds[:,i,:,:,:], heatmaps))
        combined_loss = torch.stack(combined_loss, dim=1)
        combined_loss = torch.mean(combined_loss)
        return combined_loss
